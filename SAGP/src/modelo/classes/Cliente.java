package modelo.classes;

public class Cliente {

	private String usuario;
	private long senha;
	public String getUsuario() {
		return usuario;
	}
	public void setUsuario(String usuario) {
		this.usuario = usuario;
	}
	public long getSenha() {
		return senha;
	}
	public void setSenha(long senha) {
		this.senha = senha;
	}
	
}
